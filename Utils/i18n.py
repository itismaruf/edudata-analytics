def init_language(default_language=DEFAULT_LANGUAGE):
    if "language" not in st.session_state:
        st.session_state["language"] = default_language
    if st.session_state["language"] == "tg":
        st.session_state["language"] = "tj"
    if st.session_state["language"] not in TRANSLATIONS:
        st.session_state["language"] = default_language


def get_language():
    init_language()
    return st.session_state.get("language", DEFAULT_LANGUAGE)


def set_language(language):
    if language == "tg":
        language = "tj"
    if language in TRANSLATIONS:
        st.session_state["language"] = language


def t(key, **kwargs):
    lang = get_language()
    value = TRANSLATIONS.get(lang, {}).get(key)
    if value is None:
        value = TRANSLATIONS.get("ru", {}).get(key, key)
    if kwargs:
        try:
            value = value.format(**kwargs)
        except Exception:
            pass
    return value


def _translate_text(value):
    if not isinstance(value, str):
        return value
    translated = t(value)
    if translated != value:
        return translated

    replacements = TRANSLATIONS.get(get_language(), {})
    for source in sorted(replacements.keys(), key=len, reverse=True):
        if "." in source and source.split(".", 1)[0] in {"language"}:
            continue
        target = replacements[source]
        if source and source in translated:
            translated = translated.replace(source, target)
    return translated


def _translate_option(option, original_format_func=None):
    if original_format_func:
        try:
            return _translate_text(original_format_func(option))
        except Exception:
            return _translate_text(str(option))
    return _translate_text(str(option))


def render_language_selector():
    init_language()
    options = list(LANGUAGE_OPTIONS.keys())
    current = get_language()
    index = options.index(current) if current in options else 0
    selected = st.sidebar.selectbox(
        t("language.label"),
        options,
        index=index,
        format_func=lambda item: t(f"language.{item}"),
        key="language_selector",
    )
    if selected != current:
        set_language(selected)
        st.rerun()


def _patch_text_method(obj, name):
    original = getattr(obj, name, None)
    if original is None or getattr(original, "_i18n_wrapped", False):
        return

    def wrapped(*args, **kwargs):
        args = list(args)
        text_index = 1 if args and isinstance(args[0], DeltaGenerator) else 0
        if len(args) > text_index and isinstance(args[text_index], str):
            args[text_index] = _translate_text(args[text_index])
        for key in ("label", "body", "text", "help", "placeholder"):
            if isinstance(kwargs.get(key), str):
                kwargs[key] = _translate_text(kwargs[key])
        return original(*args, **kwargs)

    wrapped._i18n_wrapped = True
    setattr(obj, name, wrapped)


def _patch_option_method(obj, name):
    original = getattr(obj, name, None)
    if original is None or getattr(original, "_i18n_wrapped", False):
        return

    def wrapped(*args, **kwargs):
        args = list(args)
        label_index = 1 if args and isinstance(args[0], DeltaGenerator) else 0
        if len(args) > label_index:
            args[label_index] = _translate_text(args[label_index])
        original_format_func = kwargs.get("format_func")
        kwargs["format_func"] = lambda item: _translate_option(item, original_format_func)
        for key in ("help", "placeholder"):
            if isinstance(kwargs.get(key), str):
                kwargs[key] = _translate_text(kwargs[key])
        return original(*args, **kwargs)

    wrapped._i18n_wrapped = True
    setattr(obj, name, wrapped)


def _patch_tabs(obj):
    original = getattr(obj, "tabs", None)
    if original is None or getattr(original, "_i18n_wrapped", False):
        return

    def wrapped(tabs, *args, **kwargs):
        translated_tabs = [_translate_text(item) for item in tabs]
        return original(translated_tabs, *args, **kwargs)

    wrapped._i18n_wrapped = True
    setattr(obj, "tabs", wrapped)


def install_streamlit_i18n():
    init_language()
    text_methods = [
        "title", "header", "subheader", "caption", "info", "warning", "error",
        "success", "markdown", "button", "form_submit_button", "text_input",
        "text_area", "file_uploader", "chat_input", "download_button", "metric",
        "checkbox", "number_input", "slider",
    ]
    option_methods = ["selectbox", "radio", "multiselect"]

    for target in (st, DeltaGenerator):
        for method in text_methods:
            _patch_text_method(target, method)
        for method in option_methods:
            _patch_option_method(target, method)
        _patch_tabs(target)
